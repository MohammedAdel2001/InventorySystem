    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String EmployeeID=jTextField1.getText();
        String Name=jTextField2.getText();
        String Email=jTextField3.getText();
        String Address=jTextField4.getText();
        String PhoneNumber=jTextField5.getText();
        
        
        if(EmployeeID.equals("")||Name.equals("")||Email.equals("")||Address.equals("")||PhoneNumber.equals("")){
            JOptionPane.showMessageDialog(null, "Some Fields are Empty");
        }
        
        if(adminrole.data().contains(EmployeeID)){
            JOptionPane.showMessageDialog(null, "The Employee with id = "+EmployeeID+" username already exist");
        }
        else{
        try {
            adminrole.addEmployee(EmployeeID,Name, Email, Address, PhoneNumber);
             JOptionPane.showMessageDialog(null, "The Employee with id = "+EmployeeID+" has been successfully added");
            
        } catch (IOException ex) {
            System.out.println("error occured");
        }
        }
        
    }//GEN-LAST:event_jButton6ActionPerformed

